$factory->define(User::class, function (Faker $faker) {

   $login = $faker->text(10);
    return [
       'login' => $login,
        'name' => $faker->name,
        'email' => $faker->unique()->safeEmail,
        'email_verified_at' => now(),
        'password' => bcrypt('secret'),
        'is_admin' => $faker->boolean(),
        'remember_token' => str_random(10),
    ];
});
